package io.kudos.base.bean.validation.support

/**
 * 常用正则表达式常量。命名尽量表达「匹配什么」而非历史缩写；与 [RegExpEnum] 一一对应。
 *
 * 按用途拆分到内部对象中，便于查找和维护；同时保留同名平铺常量，避免现有调用方被迫迁移。
 *
 * @author K
 * @author AI: Cursor
 * @since 1.0.0
 */
object RegExps {

    object Communication {

        /**
         * 规则：
         * 中国大陆 11 位手机号
         * 以 `1` 开头
         * 第二、三位需落在当前规则列出的号段内
         *
         * 例如：
         * `13800138000`
         * `15912345678`
         * `19912345678`
         */
        const val CN_MAINLAND_MOBILE = "^1(3[0-9]|4[579]|5[0-35-9]|6[0-9]|7[0-9]|8[0-9]|9[0-9])\\d{8}$"

        /**
         * 规则：
         * QQ 号
         * 只允许数字
         * 长度 5～11 位
         *
         * 例如：
         * `12345`
         * `10000`
         * `12345678901`
         */
        const val QQ_NUMBER = "^\\d{5,11}$"

        /**
         * 规则：
         * 手机或电话的纯数字串
         * 不区分手机、固话、分机
         * 只校验数字和长度 7～20 位
         *
         * 例如：
         * `1234567`
         * `02012345678`
         * `12345678901234567890`
         */
        const val PHONE_DIGITS_7_20 = "^\\d{7,20}$"

        /**
         * 规则：
         * 国内固话或手机号
         * 固话支持区号、连字符和可选分机
         * 手机号按规则内列出的号段匹配
         *
         * 例如：
         * `010-12345678`
         * `010-12345678-123`
         * `13800138000`
         */
        const val TEL_OR_CN_MOBILE =
            "^0\\d{2,3}-?\\d{7,8}$|^(0\\d{2,3})-?(\\d{7,8})-?(\\d{1,4})$|^1(3[0-9]|4[57]|5[0-35-9]|7[0-9]|8[0-35-9])\\d{8}$"

        /**
         * 规则：
         * 电子邮箱
         * 本地部分支持字母数字及 `.`、`_`、`-`
         * 域名部分按常见邮箱域名形态宽松匹配
         *
         * 例如：
         * `user@example.com`
         * `user.name@example.com`
         * `abc-123@test-mail.com`
         */
        const val EMAIL = "^[a-zA-Z0-9]+([._\\-]*[a-zA-Z0-9])*@([a-z0-9]+[-a-z0-9]*[a-z0-9]+.){1,63}[a-z0-9]+$"

        /**
         * 规则：
         * 电子邮箱或中国大陆手机号
         * 只要满足两者之一即可
         *
         * 例如：
         * `user@example.com`
         * `abc_01@test.com`
         * `13800138000`
         */
        const val MAIL_OR_CN_MOBILE =
            "^([a-z0-9]+([._\\-]*[a-z0-9])*@([a-z0-9]+[-a-z0-9]*[a-z0-9]+.){1,63}[a-z0-9]+)|(1(3[0-9]|5[0-35-9]|7[0-9]|8[0-35-9])\\d{8})$"

        /**
         * 规则：
         * 国内固定电话
         * 支持 2 位或 3 位区号
         * 支持主号码和分机号，使用 `-` 连接
         *
         * 例如：
         * `010-12345678`
         * `021-1234567`
         * `010-12345678-123`
         */
        const val CN_LANDLINE_PHONE =
            "^(((0\\d{2})[-](\\d{8})$)|(^(0\\d{3})[-](\\d{7,8})$)|(^(0\\d{2})[-](\\d{8})-(\\d+)$)|(^(0\\d{3})[-](\\d{7,8})-(\\d+)))$"

        /**
         * 规则：
         * 微信号
         * 首字符必须是字母或数字
         * 后续可含字母、数字、`-`、`_`
         * 总长度 6～20
         *
         * 例如：
         * `wechat1`
         * `wx_123456`
         * `abc-def_01`
         */
        const val WECHAT_ID = "^[a-zA-Z0-9]{1}[-_a-zA-Z0-9]{5,19}$"
    }

    object Name {

        /**
         * 规则：
         * 简短姓名
         * 允许汉字、拉丁字母和 `·`
         * 长度 2～30
         *
         * 例如：
         * `张三`
         * `王小明`
         * `John·Doe`
         */
        const val SHORT_PERSON_NAME = "^[a-z\\u4E00-\\u9FA5\\u0800-\\u4e00\\\\A-Z\\·]{2,30}$"

        /**
         * 规则：
         * 真实姓名
         * 首尾必须是字母、汉字、空格规则允许的姓名字符
         * 中间可含数字、空格、`·`、`.`、`*`
         * 不能为纯数字
         *
         * 例如：
         * `张三`
         * `John Smith`
         * `李 四`
         */
        const val REAL_PERSON_NAME = ("^[a-zA-Z\\u0020\\u4E00-\\u9FA5\\u0800-\\u4e00\\*]"
                + "[a-zA-Z0-9\\u0020\\u4E00-\\u9FA5\\u0800-\\u4e00\\·\\.\\* ]{0,28}"
                + "[a-zA-Z\\u0020\\u4E00-\\u9FA5\\u0800-\\u4e00\\*]$")

        /**
         * 规则：
         * 银行账户户名
         * 允许中英文、数字、空格、`·`、`.`、括号、`*`
         * 适用于个人或公司名称展示
         *
         * 例如：
         * `张三`
         * `王五（公司）`
         * `ABC Trading Co.`
         */
        const val BANK_ACCOUNT_HOLDER_NAME = ("^[a-zA-Z\\u4E00-\\u9FA5\\u0800-\\u4e00\\*]"
                + "[a-zA-Z0-9\\u4E00-\\u9FA5\\u0800-\\u4e00\\·\\.()（）\\* ]{0,28}"
                + "[a-zA-Z\\u4E00-\\u9FA5\\u0800-\\u4e00\\*()（）]$")

        /**
         * 规则：
         * 存款人姓名
         * 首尾必须是中英文字符
         * 中间允许空格、`.`、`·`
         *
         * 例如：
         * `张三`
         * `Jane Doe`
         * `Li Lei`
         */
        const val PAYER_DISPLAY_NAME =
            "^[a-zA-Z\\u4E00-\\u9FA5\\u0800-\\u4e00][a-zA-Z\\u4E00-\\u9FA5\\u0800-\\u4e00\\·\\. ]{0,28}[a-zA-Z\\u4E00-\\u9FA5\\u0800-\\u4e00]$"

        /**
         * 规则：
         * 昵称
         * 允许中文、英文、数字
         * 长度 3～15
         *
         * 例如：
         * `小明007`
         * `Alice88`
         * `玩家123`
         */
        const val NICK_NAME = "^[a-zA-Z0-9\\u4E00-\\u9FA5\\u0800-\\u4e00]{3,15}$"
    }

    object Text {

        /**
         * 规则：
         * 文本不得包含 `&`、`*`、`=`、`|`、`{}`、`<>`、`/`、`…`、`—`
         * 未列出的普通字符不受此规则限制
         *
         * 例如：
         * `hello world`
         * `备注信息`
         * `A_B-C.1`
         */
        const val TEXT_WITHOUT_SPECIAL_CHARS = "^[^&*=|{}<>/\\…—]*$"

        /**
         * 规则：
         * 任意字符
         * 长度 1～30
         * 常用于密保答案、自由文本短字段
         *
         * 例如：
         * `a`
         * `my answer`
         * `答案123`
         */
        const val TEXT_1_TO_30_CHARS = "^(.){1,30}$"

        /**
         * 规则：
         * 首字符必须是数字
         * 后续字符不受此规则限制
         *
         * 例如：
         * `1`
         * `1abc`
         * `9-xyz`
         */
        const val TEXT_STARTS_WITH_DIGIT = "^[0-9].*$"

        /**
         * 规则：
         * 仅含数字
         * 允许空串
         * 常用于“只输数字”的可选字段
         *
         * 例如：
         * ``
         * `12345`
         * `0001`
         */
        const val DIGITS_ONLY_OPTIONAL_EMPTY = "^[0-9]*$"

        /**
         * 规则：
         * 仅中文、拉丁字母与数字
         * 不允许空格和符号
         *
         * 例如：
         * `abc123`
         * `中国123`
         * `Kudos中国123`
         */
        const val HAN_LATIN_ALNUM = "^[0-9a-zA-Z\\u4e00-\\u9fa5]+$"

        /**
         * 上下文
         *
         * 规则：
         * 必须以 `/` 开头
         * 只用小写字母、数字、短横线 `-`
         * 多级路径用 `/` 分隔
         * 每一段不能为空
         * 不用空格、中文、下划线、连续斜杠
         * 不把 query、fragment 风格的内容塞进 path 里
         *
         * 例如：
         * `/`
         * `/kudos-base`
         * `/kudos-base/api/v1`
         * `/kudos-base/api/v1/`
         */
        const val CONTEXT = "^/(?:[a-z0-9]+(?:-[a-z0-9]+)*/?)*$"
    }

    object CharacterSet {

        /**
         * 规则：
         * 整个字符串由同一个字符重复组成
         * 重复次数至少 2 次
         *
         * 例如：
         * `aaa`
         * `1111`
         * `%%%%`
         */
        const val SINGLE_CHAR_REPEATED = "^(.)\\1+$"

        /**
         * 规则：
         * 至少一位数字
         * 只允许数字
         *
         * 例如：
         * `0`
         * `123`
         * `987654321`
         */
        const val DIGITS_NON_EMPTY = "^\\d+$"

        /**
         * 规则：
         * 仅拉丁字母
         * 允许大写和小写
         * 不允许数字和符号
         *
         * 例如：
         * `abc`
         * `ABC`
         * `AbCd`
         */
        const val LATIN_LETTERS_ONLY = "^[a-zA-Z]+$"

        /**
         * 规则：
         * 仅小写拉丁字母
         * 不允许大写字母、数字、符号
         *
         * 例如：
         * `abc`
         * `kudos`
         * `username`
         */
        const val LATIN_LOWERCASE_ONLY = "^[a-z]+$"

        /**
         * 规则：
         * 仅小写字母与数字
         * 不能是纯数字
         * 至少包含一个小写字母
         *
         * 例如：
         * `abc123`
         * `a1`
         * `user2026`
         */
        const val LOWERCASE_ALNUM_NOT_ALL_DIGITS = "^(?!\\d+$)[\\da-z]+$"

        /**
         * 规则：
         * 仅 ASCII 数字
         * 至少一位
         * 不接受全角数字
         *
         * 例如：
         * `0`
         * `123456`
         * `987654321`
         */
        const val ASCII_DIGITS_ONLY = "^[0-9]+$"

        /**
         * 规则：
         * 仅拉丁字母与数字
         * 不允许空格、下划线、连字符和其他符号
         *
         * 例如：
         * `abc`
         * `ABC123`
         * `Abc123`
         */
        const val LATIN_ALNUM_ONLY = "^[a-zA-Z0-9]+$"

        /**
         * 规则：
         * 仅拉丁字母、数字、连字符、下划线
         * 至少 1 位
         *
         * 例如：
         * `user`
         * `user_name`
         * `user-name_01`
         */
        const val LATIN_ALNUM_DASH_UNDERSCORE = "^[a-zA-Z0-9_-]+$"

        /**
         * 规则：
         * 宽松标识符/变量名
         * 允许大小写字母、数字、下划线、短横线
         * 首字符只能是字母或下划线
         * 后续字符允许字母、数字、下划线、短横线
         *
         * 例如：
         * `name`
         * `_user-name`
         * `Abc_123-x`
         */
        const val RELAXED_VAR_NAME = "^[A-Za-z_][A-Za-z0-9_-]*$"

        /**
         * 规则：
         * 标识符
         * 首字符必须是字母或下划线
         * 后续允许字母、数字、下划线
         *
         * 例如：
         * `name`
         * `_value`
         * `user_name1`
         */
        const val VAR_NAME = "^[A-Za-z_][A-Za-z0-9_]*$"

        /**
         * 规则：
         * 小写 slug
         * 只允许小写字母、数字、连字符
         * 不允许连续连字符分段为空
         *
         * 例如：
         * `article`
         * `user-profile`
         * `user-profile-01`
         */
        const val SLUG_KEBAB_LOWERCASE = "^[a-z0-9]+(?:-[a-z0-9]+)*$"
    }

    object Security {

        /**
         * 规则：
         * 登录密码
         * 长度 6～20
         * 允许字母、数字和常见 ASCII 符号
         *
         * 例如：
         * `Abc123`
         * `Abc123!`
         * `Pass_word#1`
         */
        const val LOGIN_PASSWORD = "^[A-Za-z0-9~\\\\\\-!@#$%^&*()_+\\{\\}\\[\\]|\\:;'\"<>,./?]{6,20}$"

        /**
         * 规则：
         * 6 位数字安全码 / PIN
         * 只允许数字
         * 长度固定为 6
         *
         * 例如：
         * `123456`
         * `000001`
         * `987654`
         */
        const val SECURITY_PIN_SIX_DIGITS = "^[0-9]{6}$"

        /**
         * 规则：
         * 密码强度分类中的“仅字母”
         * 规则与 [CharacterSet.LATIN_LETTERS_ONLY] 完全一致
         *
         * 例如：
         * `abc`
         * `OnlyText`
         * `Password`
         */
        const val PASSWORD_STRENGTH_LETTERS_ONLY = CharacterSet.LATIN_LETTERS_ONLY

        /**
         * 规则：
         * 密码强度分类中的“仅数字”
         * 规则与 [CharacterSet.ASCII_DIGITS_ONLY] 完全一致
         *
         * 例如：
         * `123456`
         * `987654`
         * `20260403`
         */
        const val PASSWORD_STRENGTH_DIGITS_ONLY = CharacterSet.ASCII_DIGITS_ONLY

        /**
         * 规则：
         * 密码强度分类中的“字母与数字”
         * 允许大小写字母和数字
         * 不允许符号
         *
         * 例如：
         * `Abc123`
         * `abc999`
         * `PASS2026`
         */
        const val PASSWORD_STRENGTH_LETTERS_AND_DIGITS = "^[0-9a-zA-Z]+$"

        /**
         * 规则：
         * 密码强度分类中的“字母、数字与常用符号”
         * 允许大小写字母、数字和 ASCII 常见符号
         *
         * 例如：
         * `Abc123!`
         * `P@ssw0rd#`
         * `A1_b2-C3`
         */
        const val PASSWORD_STRENGTH_WITH_SYMBOLS =
            "^[A-Za-z0-9~!@#$%^&*()_+\\\\{\\\\}\\\\[\\\\]|\\\\:;\\'\\\"<>,./?]+$"
    }

    object Numeric {

        /**
         * 规则：
         * 正整数文本
         * 不允许负号、小数
         * 不允许以 `0` 开头
         *
         * 例如：
         * `1`
         * `42`
         * `999999`
         */
        const val POSITIVE_INT_TEXT = "^[1-9]\\d*$"

        /**
         * 规则：
         * 让分或比分类比分串
         * 支持可选负号
         * 支持整数、小数、`/` 或 `-` 作为分隔形式
         *
         * 例如：
         * `1.5`
         * `1.5/2`
         * `-0.5`
         */
        const val SCORE_OR_HANDICAP_TEXT =
            "^(\\-?)((?:[0-9]{1,4})(?:\\.\\d{1,2})?)(\\/?|\\-?)((?:[0-9]{1,4}|0)(?:\\.\\d{1,2})?)?$"

        /**
         * 规则：
         * 正数文本
         * 允许整数或小数
         * 不允许负号
         * 禁止多余前导零
         *
         * 例如：
         * `1`
         * `12`
         * `12.5`
         */
        const val POSITIVE_DECIMAL_TEXT = "^(?!(0[0-9]{0,}$))[0-9]{1,}[.]{0,}[0-9]{0,}$"

        /**
         * 规则：
         * 正数
         * 可以是大于 0 的整数
         * 也可以是 `0.xxx` 且小数部分至少有一个非零位
         *
         * 例如：
         * `8`
         * `10.5`
         * `0.25`
         */
        const val POSITIVE_NUMBER_TEXT = "^([1-9]\\d*\\.?\\d*)|(0\\.\\d*[1-9])$"

        /**
         * 规则：
         * 整数文本
         * 可带 `+` 或 `-`
         * 不允许小数点
         *
         * 例如：
         * `0`
         * `-10`
         * `+8`
         */
        const val SIGNED_INTEGER_TEXT = "^(-|\\+)?\\d+$"

        /**
         * 规则：
         * 比特币数量
         * 不允许全零
         * 整数部分可为正整数
         * 小数部分最多 8 位
         *
         * 例如：
         * `1`
         * `0.12345`
         * `1.00000001`
         */
        const val BTC_AMOUNT_TEXT = "^(?!0+(?:\\.0+)?$)(((?:[1-9]\\d*(\\.\\d{1,8})?))|(0\\.\\d{1,5}))?$"

        /**
         * 规则：
         * 空串或正整数
         * 如果有值，则必须是大于 0 的整数
         *
         * 例如：
         * ``
         * `1`
         * `25`
         */
        const val EMPTY_OR_POSITIVE_INT_TEXT = "^([0-9]*[1-9][0-9]*)?$"

        /**
         * 规则：
         * 最多 9 位数字
         * 可为空
         * 不允许其他字符
         *
         * 例如：
         * ``
         * `1`
         * `123456789`
         */
        const val DIGITS_AT_MOST_9 = "^\\d{0,9}$"

        /**
         * 规则：
         * 金额
         * 可带负号
         * 整数部分 0 或非零开头整数
         * 小数部分最多 2 位
         *
         * 例如：
         * `0`
         * `12.5`
         * `-12.50`
         */
        const val SIGNED_AMOUNT_LOOSE = "^\\-?([1-9]\\d{0,9}|0)([.]?|(\\.\\d{1,2})?)$"

        /**
         * 规则：
         * 非零金额
         * 金额不能为 `0` 或 `0.00`
         * 小数部分最多 2 位
         *
         * 例如：
         * `0.01`
         * `1`
         * `99.99`
         */
        const val AMOUNT_NONZERO_TWO_DECIMALS = "^(?!0+(?:\\.0+)?$)(?:[1-9]\\d*|0)(?:\\.\\d{1,2})?$"

        /**
         * 规则：
         * 整数百分比
         * 只允许整数
         * 取值范围 0～100
         *
         * 例如：
         * `0`
         * `85`
         * `100`
         */
        const val PERCENT_INTEGER_0_100 = "^(?:100|[1-9]\\d?|0)$"
    }

    object Network {

        /**
         * 规则：
         * IPv4 点分十进制地址
         * 共 4 段
         * 每段取值范围 0～255
         *
         * 例如：
         * `127.0.0.1`
         * `192.168.1.1`
         * `255.255.255.255`
         */
        const val IPV4 = "^(25[0-5]|2[0-4]\\d|[0-1]?\\d?\\d)(\\.(25[0-5]|2[0-4]\\d|[0-1]?\\d?\\d)){3}$"

        /**
         * 规则：
         * URL，支持 `http`、`https`、`ftp`、`sftp`
         * 允许域名、IPv4、端口、路径、query、fragment
         * 属于宽松匹配，不等同完整 RFC 严格校验
         *
         * 例如：
         * `https://example.com`
         * `http://example.com:8080/api`
         * `https://example.com/search?q=test#top`
         */
        const val HTTP_URL = (("^(https?|s?ftp):\\/\\/(((([A-Za-z]|\\d|-|\\.|_|~|"
                + "[\\u00A0-\\uD7FF\\uF900-\\uFDCF\\uFDF0-\\uFFEF])|(%[\\da-f]{2})|[!\\$&'\\(\\)\\*\\+,;=]|:)*@)?(((\\d|"
